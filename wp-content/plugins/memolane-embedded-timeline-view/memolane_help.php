	<h2><?php echo _e( 'Memolane Embed Help' ); ?></h2>
	<p>This WordPress plugin adds a simple shortcode to embed the awesome Memolane media timeline view in your blog posts and pages.<br/>
	When adding a post with a Memolane embed, first add and configure your lane via the Memolane plugin administrator menu.</p>

	<h3>How To</h3>

	<iframe width="560" height="315" src="http://www.youtube.com/embed/66aQDG4P-Y8" frameborder="0" allowfullscreen></iframe>

    <p>Find "Plug-ins" on the left hand toolbar and click "add new". Here you can search for the Memolane plug-in by typing in "Memolane".</p>

    <p><img src="http://memolane.com/img/embedded/how-to-1.png" /></p>

    <p>To begin the install process click "install" in the upper right hand corner. You can also install the plug-in by uploading the zip file on this panel or unzipping the contents into the appropriate plug-ins folder. Once the plug-in has been successfully installed, click "activate plug-in" located under the Memolane plug-in in the main plug-in page.</p>

    <p><img src="http://memolane.com/img/embedded/how-to-2.png" /></p>

    <p>The Memolane plug-in should now be visible on the left side of the toolbar. Click on "Memolane" and then click on "add new".You will be presented with a screen that asks for your lane details and modification options. We suggest that before completing this process you should log into your Memolane account and review the lane you wish to embed. Review the selected filters to make sure that it contains all your desired content. You can always go back and edit these settings once you have embedded the lane.</p>

    <p>Return to the Memolane plug-in page. After you are content with the lane content and filters, fill out the desired lane specifics: username and lane title.Next set your size parameters for the embedded lane. Give your lane enough space to show off your wonderful content! A lane that is 200 px by 200px will look cramped and too small. Choose the color you want for the background and border. You can set both to transparent if you want it to easily blend in with your website.</p>

    <p>The following parameters are available (these are just sample values--replace with yours):</p>

    <p><strong> Usernam</strong>e - the memolane user name associated with this lane (ex: Eric) can be found on the lane url, this is how you would decipher the URL: memolane.com/username/lane-title</p>

    <p><strong>Title</strong> - the case insensitive title of the lane to be embedded (ex: Memolane Journey). If left blank memolane.com/username is loaded as the embedded lane</p>

    <p><strong> Width</strong> - the width, in px (pixels) or % (percentage) to display for the embedded lane. Default is 500px. If you're having trouble start with 100% and work from there.</p>

    <p><strong> Height</strong> - Same as width, but with respect to height.</p>

    <p><strong> Background Color</strong> - the background color of the iframe of the embedded lane. Use hex codes or 'transparent' for this. Hex codes can be found here: http://www.december.com/html/spec/colorhex.html</p>

    <p><strong> Border</strong> - The border of the iframe of the embedded lane. Use a css value or default. (ex: 1px solid white)
    Click "save" to capture all of your parameters. Copy the short code that will appear at the bottom of the parameter page.</p>

    <p><img src="http://memolane.com/img/embedded/how-to-3.png" /></p>

    <p>Paste the short code into the HTML section of your WordPress blog.</p>

    <p><img src="http://memolane.com/img/embedded/how-to-4.png" /></p>

    <p>Share with friends!</p>

